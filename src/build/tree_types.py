"""Tree-layer dataclasses for the vNext MemoryForest build pipeline."""

from __future__ import annotations

from dataclasses import dataclass, field


@dataclass
class MemTreeNode:
    """One node in a MemTree (mutable — state changes during build and updates)."""

    node_id: str
    tree_id: str
    level: int               # 0 = single payload leaf; higher = closer to root
    time_start: float
    time_end: float
    summary: str             # empty until generated by SummaryManager
    summary_dirty: bool      # True when child facts changed since last summary gen
    child_ids: list[str]     # node_ids for level>0; exactly one fact_id/cell_id for level=0
    item_count: int          # total facts/cells in this subtree
    parent_id: str | None = None
    prev_leaf_id: str | None = None
    next_leaf_id: str | None = None


@dataclass
class SessionLeaf:
    """Leaf entry in a session tree — one MemCell's worth of content."""

    cell_id: str             # globally unique back-reference key
    session_id: str
    cell_index: int          # position within session (for ±1 context window)
    time_start: float
    time_end: float
    raw_turns: list[dict]    # [{"role": str, "content": str}]
    raw_text: str            # concatenated turns for fast display
    fact_ids: list[str]      # canonical fact IDs extracted from this cell


@dataclass
class MemTree:
    """A fully built or in-progress memory tree."""

    tree_id: str             # e.g. "entity:user", "scene:c3a1_fitness", "session:xxx"
    tree_type: str           # "session" | "entity" | "scene"
    tree_key: str            # entity key, cluster_id, or session_id
    label: str               # human-readable ("user", "Rachel", "fitness")
    k: int                   # fan-out per internal node
    root_node_id: str
    nodes: dict[str, MemTreeNode]           # node_id → node
    session_leaves: dict[str, SessionLeaf]  # cell_id → leaf (session trees only)
    fact_ids_ordered: list[str]             # time-sorted fact_ids (entity/scene only)
    centroid: list[float] | None            # root summary embedding (for FAISS)
    version: int             # bumped on every structural change
    lifecycle_state: str     # "active" | "lazy" | "suppressed"
    deactivated: bool        # True after split; TreeStore redirects reads
    replacement_tree_ids: list[str]         # set when deactivated after split
    created_at: float
    updated_at: float
    dirty_l0_node_ids: set[str] = field(default_factory=set)
    dirty_internal_node_ids_by_level: dict[int, set[str]] = field(default_factory=dict)


@dataclass
class SceneCluster:
    """One dynamic scene cluster managed by SceneRouter."""

    cluster_id: str
    label: str
    scope_key: str
    centroid: list[float]    # running-average embedding
    fact_count: int
    fact_ids: list[str]      # insertion-order
    created_at: float
    updated_at: float
    latest_fact_time: float | None
    parent_cluster_id: str | None   # set when spawned from a split
    topic_histogram: dict[str, int] = field(default_factory=dict)
    domain_histogram: dict[str, int] = field(default_factory=dict)


@dataclass(frozen=True)
class TreeCard:
    """Lightweight tree descriptor stored in the root FAISS index."""

    tree_id: str
    tree_type: str
    label: str
    time_start: float
    time_end: float
    root_summary: str
    item_count: int


@dataclass(frozen=True)
class SummaryRequest:
    """One LLM call to generate a single tree node summary."""

    request_id: str          # "{tree_id}:{node_id}"
    tree_id: str
    node_id: str
    tree_type: str           # "session" | "entity" | "scene"
    tree_label: str
    level: int
    time_start: float
    time_end: float
    input_text: str          # concatenated child summaries or fact texts


@dataclass(frozen=True)
class SummaryResult:
    """Result from one LLM summary call."""

    request_id: str
    tree_id: str
    node_id: str
    summary: str
    elapsed_sec: float
    error: str | None = None


@dataclass(frozen=True)
class TreeBuildResult:
    """Summary of one tree build or rebuild."""

    tree_id: str
    tree_type: str
    label: str
    node_count: int
    leaf_count: int
    summary_calls: int
    elapsed_sec: float
    error: str | None = None


@dataclass(frozen=True)
class EntityCandidate:
    """Lifecycle-tracked entity candidate used by EntityRouter."""

    entity_key: str
    display_label: str
    support_count: int
    distinct_fact_count: int
    distinct_session_count: int
    first_seen_at: float | None
    last_seen_at: float | None
    fact_ids: list[str] = field(default_factory=list)
    session_ids: list[str] = field(default_factory=list)
    state: str = "lazy"


@dataclass(frozen=True)
class EntityManifestRecord:
    """Persisted lazy/suppressed entity metadata."""

    entity_key: str
    display_label: str
    state: str
    support_count: int
    distinct_fact_count: int
    distinct_session_count: int
    fact_ids: list[str] = field(default_factory=list)


@dataclass(frozen=True)
class SceneBootstrapState:
    """Persisted scene bootstrap state."""

    initialized: bool
    buffered_fact_ids: list[str] = field(default_factory=list)
    cluster_count: int = 0
